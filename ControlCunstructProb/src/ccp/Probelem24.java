package ccp;

public class Probelem24 {
	public static void sumOf(int n)
	{
		//initiating the integer as fact as 1
		int sum =0;
		//Loop for checking every value of i till n
		for(int i=1;i<=n;i++)
		{
			//Adding all value of i till n
			sum +=i;
		}
		System.out.println("sum of all n nos = "+sum);
	}
}
